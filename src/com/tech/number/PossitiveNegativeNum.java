package com.tech.number;

public class PossitiveNegativeNum {

	public static void main(String[] args) {
		
		int num=0;
		if(num>0) {
			System.out.println("Its Possitive Number "+num);
		}else if(num<0){
			System.out.println("Its Negative Number "+num);
		}else {
			System.out.println("Its 0");
		}
	}
}
